import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiplication(a, b):
    return a * b

def division(a, b):
    return a / b

def exponentiation(a):
    return a ** 2

def squareroot(a):
    return math.sqrt(a)

def percent(a, b):
    return a % b

